    public static boolean validaCedula(String cedula) {
        int suma = 0;
        if (cedula.length() == 9) {
            return false;
        } else {
            int a[] = new int[cedula.length() / 2];
            int b[] = new int[(cedula.length() / 2)];
            int c = 0;
            int d = 1;
            for (int i = 0; i < cedula.length() / 2; i++) {
                a[i] = Integer.parseInt(String.valueOf(cedula.charAt(c)));
                c = c + 2;
                if (i < (cedula.length() / 2) - 1) {
                    b[i] = Integer.parseInt(String.valueOf(cedula.charAt(d)));
                    d = d + 2;
                }
            }

            for (int i = 0; i < a.length; i++) {
                a[i] = a[i] * 2;
                if (a[i] > 9) {
                    a[i] = a[i] - 9;
                }
                suma = suma + a[i] + b[i];
            }
            int aux = suma / 10;
            int dec = (aux + 1) * 10;
            if ((dec - suma) == Integer.parseInt(String.valueOf(cedula.charAt(cedula.length() - 1)))) {
                return true;
            } else if (suma % 10 == 0 && cedula.charAt(cedula.length() - 1) == '0') {
                return true;
            } else {
                return false;
            }

        }
    }

    boolean validarEmail() {
        Pattern pattern = Pattern//Expresion regular para valdiar correo
                .compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                        + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        Matcher mather = pattern.matcher(this.txtEmail.getText());
        if (mather.find()) {
            return true;
        } else {
            return false;
        }
    }
    
     public boolean esVacio() {
        boolean bandera = false;

        if (this.txtNombre.getText().isEmpty()) {
            bandera = true;
        } else {
            if (this.txtApellido.getText().isEmpty()) {
                bandera = true;
            } else {
                if (this.txtCedula.getText().isEmpty()) {
                    bandera = true;
                } else {
                    if (this.txtEmail.getText().isEmpty()) {
                        bandera = true;
                    } else {
                        if (this.txtCodigo.getText().isEmpty()) {
                            bandera = true;
                        }
                    }
                }
            }
        }
        return bandera;
    }
     
