    private void add() {
        JTextField[] txts = {txtNombre, txtApellido, txtCedula, txtEmail, txtCodigo};
        if (new Validation().isEmpty(txts) && new Validation().isEmail(txtEmail)) {
            Usuario objU = new Usuario();
            objU.setNombre(txtNombre.getText());
            objU.setApellido(txtApellido.getText());
            objU.setCedula(txtCedula.getText());
            objU.setEmail(txtEmail.getText());
            objU.setCodigo(txtCodigo.getText());
            Combo cmb = (Combo) cmbDepartamento.getSelectedItem();
            objU.setObjDepartamento(new Departamento(cmb.getCodigo(), cmb.getTexto()));
            System.out.println();
            if (new daoUsuario().create(objU)) {
                JOptionPane.showMessageDialog(null, "Guardado");
            } else {
                JOptionPane.showMessageDialog(null, "NO Guardado");
            }
            clearTextField();
//            optionAction = -1;
//            enabled(false);
//            getData();
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese el campo");
        }
    }
